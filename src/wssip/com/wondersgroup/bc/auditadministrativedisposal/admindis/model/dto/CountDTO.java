package com.wondersgroup.bc.auditadministrativedisposal.admindis.model.dto;


/**
 * 
 * @ClassName: CountDTO 
 * @Description: 统计用到的DTO 
 * @author chenlin
 * @date 2014-8-1 上午10:33:37 
 *
 */
public class CountDTO {

	private String label;//统计的类型
	private Double value;//统计的值
	private String sum_text;//名称，如合计
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	public Double getValue() {
		return value;
	}
	public void setValue(Double value) {
		this.value = value;
	}
	public String getSum_text() {
		return sum_text;
	}
	public void setSum_text(String sumText) {
		sum_text = sumText;
	}
	
	
}
